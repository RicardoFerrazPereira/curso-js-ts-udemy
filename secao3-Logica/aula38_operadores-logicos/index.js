/*
OPERADORES LÓGICOS

&& => AND => E -> Todas as expressões precisam ser verdadeiras
|| => OR => OU -> Uma das expressões são verdadeiras 
! => NOT => NÂO -> Negação

*/
console.log(true && true)
console.log(true && false)
console.log(false && false)

const operadorOr = false || true;
console.log(operadorOr)
console.log(false || false)
console.log(true || true)

console.log(true);
console.log(!true);
console.log(!false);

